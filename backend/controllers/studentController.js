// Roll-number prefix from a class: "<class number padded to 2><section>",
// e.g. { name: 'Class 9', section: 'A' } → "09A". Non-numeric class names fall
// back to the cleaned uppercased name (e.g. "LKG" → "LKGA").
function classRollPrefix(cls) {
  const name = (cls?.name || '').trim();
  const m = name.match(/\d+/);
  const clsCode = m ? String(parseInt(m[0], 10)).padStart(2, '0') : name.toUpperCase().replace(/[^A-Z0-9]/g, '').slice(0, 4);
  const sec = (cls?.section || '').toUpperCase();
  return `${clsCode}${sec}`;
}

// Re-assign roll numbers (e.g. 09A01, 09A02…) to active students in a class in
// alphabetical name order. Students whose roll was set manually keep it (their
// alphabetical slot is still consumed so the auto rolls around them stay aligned).
async function renumberClassRolls(schoolId, classId) {
  if (!classId) return;
  const cls = await Class.findOne({ _id: classId, school: schoolId }).select('name section').lean();
  if (!cls) return;
  const prefix = classRollPrefix(cls);
  const students = await Student.find({ school: schoolId, currentClass: classId, status: 'active' })
    .select('name rollNumber rollNumberManual').lean();
  students.sort((a, b) => (a.name || '').localeCompare(b.name || '', undefined, { sensitivity: 'base', numeric: true }));
  const ops = [];
  students.forEach((s, i) => {
    if (s.rollNumberManual) return;
    const roll = `${prefix}${String(i + 1).padStart(2, '0')}`;
    if (s.rollNumber !== roll) ops.push({ updateOne: { filter: { _id: s._id }, update: { $set: { rollNumber: roll } } } });
  });
  if (ops.length) await Student.bulkWrite(ops);
}

